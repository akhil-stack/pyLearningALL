# Find if given number is equal , greater , or less than

Num_1 = int(input("Enter the first number :- "))
Num_2 = int(input("Enter the second number :- "))
a = (Num_1 > Num_2)
b = (Num_1 < Num_2)
c = (Num_1 == Num_2)
print("Num_1 is greater than Num_2", a)
print("Num_1 is less than Num_2", b)
print("Num_1 is equal to Num_2", c)
